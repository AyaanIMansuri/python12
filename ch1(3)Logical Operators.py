# Python program demonstrating logical operators

# Define two boolean variables
x = True
y = False

# Perform logical operations
logical_and = x and y
logical_or = x or y
logical_not_x = not x
logical_not_y = not y

# Display the results
print(f"{x} and {y} = {logical_and}")
print(f"{x} or {y} = {logical_or}")
print(f"not {x} = {logical_not_x}")
print(f"not {y} = {logical_not_y}")
